import React, { useState, useRef, useEffect } from 'react';
import { Card, CardContent, CardDescription, CardHeader, CardTitle } from '@/components/ui/card';
import { Button } from '@/components/ui/button';
import { Input } from '@/components/ui/input';
import { Label } from '@/components/ui/label';
import { Textarea } from '@/components/ui/textarea';
import { Switch } from '@/components/ui/switch';
import { toast } from 'sonner';
import { Upload, Image as ImageIcon, Settings } from 'lucide-react';
import { supabase } from '@/integrations/supabase/client';
import { Checkbox } from '@/components/ui/checkbox';
import { Select, SelectContent, SelectItem, SelectTrigger, SelectValue } from '@/components/ui/select';
import { useSetting } from '@/hooks/useSettings';
import { useCategories } from '@/hooks/useCategories';

interface PhotoUploadManagerProps {
  onPhotoUploaded?: () => void;
}

interface Category {
  id: string;
  ad: string;
  tip: string;
}

export const PhotoUploadManager: React.FC<PhotoUploadManagerProps> = ({ onPhotoUploaded }) => {
  const [photos, setPhotos] = useState<FileList | null>(null);
  const [title, setTitle] = useState('');
  const [description, setDescription] = useState('');
  const [addLogo, setAddLogo] = useState(true);
  const [isUploading, setIsUploading] = useState(false);
  const [selectedUsageArea, setSelectedUsageArea] = useState<string>('');
  const [selectedCategory, setSelectedCategory] = useState<string>('');
  const [filteredCategories, setFilteredCategories] = useState<Category[]>([]);
  
  const photoInputRef = useRef<HTMLInputElement>(null);
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const logoImg = useRef<HTMLImageElement>(null);
  
  const firmaLogo = useSetting('firma_logo_url');

  // T√ºm kategorileri √ßek
  const { data: allCategories = [] } = useCategories();

  const usageAreas = [
    { id: 'ana-sayfa-slider', label: 'Ana Sayfa Slider', categoryType: null },
    { id: 'kumlama', label: 'Kumlama Sayfasƒ±', categoryType: 'kumlama' },
    { id: 'tabela', label: 'Tabela Sayfasƒ±', categoryType: 'tabela' },
    { id: 'arac-giydirme', label: 'Ara√ß Giydirme Sayfasƒ±', categoryType: 'arac-giydirme' },
    { id: 'referanslar', label: 'Referanslar Sayfasƒ±', categoryType: null }
  ];

  // Kullanƒ±m alanƒ± deƒüi≈ütiƒüinde kategorileri filtrele
  useEffect(() => {
    if (selectedUsageArea) {
      const usageArea = usageAreas.find(area => area.id === selectedUsageArea);
      if (usageArea?.categoryType) {
        const filtered = allCategories.filter(cat => cat.tip === usageArea.categoryType);
        setFilteredCategories(filtered);
        console.log(`üìã ${usageArea.label} i√ßin kategoriler:`, filtered);
      } else {
        setFilteredCategories([]);
      }
      // Kategori se√ßimini temizle
      setSelectedCategory('');
    } else {
      setFilteredCategories([]);
      setSelectedCategory('');
    }
  }, [selectedUsageArea, allCategories]);

  const addWatermark = (canvas: HTMLCanvasElement, ctx: CanvasRenderingContext2D) => {
    if (!addLogo || !firmaLogo || !logoImg.current) return;
    
    // Logo boyutunu %42 yap
    const logoWidth = canvas.width * 0.42; // %42 boyut
    const logoHeight = (logoImg.current.height / logoImg.current.width) * logoWidth;
    
    // Canvas'ƒ± kaydet
    ctx.save();
    
    // Ortaya √ßevir ve √ßapraz a√ßƒ± ver (-30 derece)
    ctx.translate(canvas.width / 2, canvas.height / 2);
    ctx.rotate(-Math.PI / 6); // -30 derece
    ctx.translate(-logoWidth / 2, -logoHeight / 2);
    
    // Logo ≈üeffaflƒ±ƒüƒ± - %12 g√∂r√ºn√ºrl√ºk
    ctx.globalAlpha = 0.12; // %12 g√∂r√ºn√ºrl√ºk
    ctx.drawImage(logoImg.current, 0, 0, logoWidth, logoHeight);
    
    // Canvas'ƒ± geri y√ºkle
    ctx.restore();
  };

  const resizeImage = (file: File, maxWidth: number = 1920, maxHeight: number = 1080): Promise<Blob> => {
    return new Promise((resolve, reject) => {
      const canvas = canvasRef.current!;
      const ctx = canvas.getContext('2d')!;
      const img = new Image();
      
      img.onload = () => {
        try {
          // Calculate new dimensions
          let { width, height } = img;
          
          if (width > maxWidth || height > maxHeight) {
            const ratio = Math.min(maxWidth / width, maxHeight / height);
            width *= ratio;
            height *= ratio;
          }
          
          canvas.width = width;
          canvas.height = height;
          
          // Draw resized image
          ctx.drawImage(img, 0, 0, width, height);
          
          // Logo filigran ekle
          addWatermark(canvas, ctx);
          
          canvas.toBlob((blob) => {
            if (blob) {
              resolve(blob);
            } else {
              reject(new Error('Canvas to blob conversion failed'));
            }
          }, 'image/jpeg', 0.85);
        } catch (error) {
          console.error('Image processing error:', error);
          reject(error);
        }
      };
      
      img.onerror = () => reject(new Error('Image load failed'));
      img.src = URL.createObjectURL(file);
    });
  };

  const handleUpload = async () => {
    if (!photos || photos.length === 0) {
      toast.error('L√ºtfen en az bir fotoƒüraf se√ßin');
      return;
    }

    if (!selectedUsageArea) {
      toast.error('L√ºtfen bir kullanƒ±m alanƒ± se√ßin');
      return;
    }

    setIsUploading(true);
    console.log('üîÑ Upload ba≈ülatƒ±lƒ±yor...', photos.length, 'fotoƒüraf');

    try {
      const uploadPromises = Array.from(photos).map(async (file, index) => {
        try {
          console.log(`üì∏ ƒ∞≈üleniyor ${index + 1}/${photos.length}:`, file.name);
          
          // Resize and add watermark
          const processedBlob = await resizeImage(file);
          
          // Generate unique filename
          const timestamp = Date.now();
          const randomId = Math.random().toString(36).substring(2);
          const extension = file.name.split('.').pop();
          const fileName = `photo_${timestamp}_${randomId}_${index}.${extension}`;
          
          console.log(`‚¨ÜÔ∏è Y√ºkleniyor: ${fileName}`);
          
          // Upload to Supabase Storage
          const { data: storageData, error: storageError } = await supabase.storage
            .from('fotograflar')
            .upload(fileName, processedBlob, {
              contentType: 'image/jpeg',
              upsert: false
            });

          if (storageError) throw storageError;

          // G√∂rsel tipini belirle
          let gorselTipi = 'galeri';
          if (selectedUsageArea === 'ana-sayfa-slider') {
            gorselTipi = 'slider';
          } else if (selectedUsageArea === 'referanslar') {
            gorselTipi = 'referans_logo';
          }

          // Kategori bilgilerini al
          const selectedCategoryData = selectedCategory ? filteredCategories.find(c => c.id === selectedCategory) : null;
          
          // Save to database
          const { error: dbError } = await supabase
            .from('fotograflar')
            .insert({
              baslik: file.name.replace(/\.[^/.]+$/, ""), // Dosya adƒ±nƒ± uzantƒ±sƒ±z olarak ba≈ülƒ±k yap
              aciklama: "", // A√ßƒ±klama bo≈ü bƒ±rak
              dosya_yolu: storageData.path,
              kategori_id: selectedCategory && selectedCategory.trim() !== '' ? selectedCategory : null,
              kategori_adi: selectedCategoryData?.ad || null,
              kullanim_alani: [selectedUsageArea],
              gorsel_tipi: gorselTipi,
              mime_type: 'image/jpeg',
              boyut: processedBlob.size,
              logo_eklendi: addLogo && firmaLogo ? true : false,
              aktif: true,
              sira_no: 0
            });

          if (dbError) {
            console.error('‚ùå Veritabanƒ± hatasƒ±:', dbError);
            console.error('üìä G√∂nderilen veri:', {
              baslik: file.name.replace(/\.[^/.]+$/, ""),
              dosya_yolu: storageData.path,
              kategori_id: selectedCategory,
              kategori_adi: selectedCategoryData?.ad,
              kullanim_alani: [selectedUsageArea],
              gorsel_tipi: gorselTipi
            });
            throw dbError;
          }
          console.log(`‚úÖ Tamamlandƒ±: ${fileName}`);
          console.log('üìä Veritabanƒ±na kaydedilen veri:', {
            baslik: file.name.replace(/\.[^/.]+$/, ""),
            dosya_yolu: storageData.path,
            kategori_id: selectedCategory,
            kategori_adi: selectedCategoryData?.ad,
            kullanim_alani: [selectedUsageArea],
            gorsel_tipi: gorselTipi
          });
          
          return fileName;
        } catch (error) {
          console.error(`‚ùå Hata (${index + 1}):`, error);
          throw error;
        }
      });

      await Promise.all(uploadPromises);
      console.log('üéâ T√ºm fotoƒüraflar ba≈üarƒ±yla y√ºklendi');

      toast.success(`${photos.length} fotoƒüraf ba≈üarƒ±yla y√ºklendi`);
      
      // Reset form
      setPhotos(null);
      setTitle('');
      setDescription('');
      setSelectedUsageArea('');
      setSelectedCategory('');
      if (photoInputRef.current) photoInputRef.current.value = '';
      
      onPhotoUploaded?.();
      
    } catch (error) {
      console.error('‚ùå Upload hatasƒ±:', error);
      toast.error('Fotoƒüraf y√ºklenirken hata olu≈ütu: ' + error.message);
    } finally {
      setIsUploading(false);
      console.log('üîÑ Upload i≈ülemi tamamlandƒ±');
    }
  };

  // Logo y√ºkleme
  useEffect(() => {
    if (firmaLogo && addLogo) {
      const img = new Image();
      img.crossOrigin = 'anonymous';
      img.onload = () => {
        logoImg.current = img;
        console.log('‚úÖ Logo filigran i√ßin hazƒ±r');
      };
      img.onerror = () => {
        console.warn('‚ö†Ô∏è Logo y√ºklenemedi, filigran eklenmeyecek');
      };
      img.src = firmaLogo;
    }
  }, [firmaLogo, addLogo]);

  return (
    <div className="space-y-6">
      <canvas ref={canvasRef} style={{ display: 'none' }} />
      
      {/* Photo Upload */}
      <Card>
        <CardHeader>
          <CardTitle className="flex items-center gap-2">
            <ImageIcon className="h-5 w-5" />
            Fotoƒüraf Y√ºkleme
          </CardTitle>
          <CardDescription>
            Fotoƒüraflar otomatik olarak boyutlandƒ±rƒ±lƒ±r {addLogo && firmaLogo && "ve logo filigranƒ± eklenir"}
          </CardDescription>
        </CardHeader>
        <CardContent className="space-y-4">
          <div>
            <Label htmlFor="photos">Fotoƒüraflar</Label>
            <Input
              id="photos"
              type="file"
              multiple
              accept="image/*"
              ref={photoInputRef}
              onChange={(e) => setPhotos(e.target.files)}
            />
            {photos && (
              <p className="text-sm text-muted-foreground mt-1">
                {photos.length} fotoƒüraf se√ßildi
              </p>
            )}
          </div>

          <div>
            <Label htmlFor="usageArea">Kullanƒ±m Alanƒ±</Label>
            <Select value={selectedUsageArea} onValueChange={setSelectedUsageArea}>
              <SelectTrigger>
                <SelectValue placeholder="Kullanƒ±m alanƒ± se√ßin" />
              </SelectTrigger>
              <SelectContent>
                {usageAreas.map((area) => (
                  <SelectItem key={area.id} value={area.id}>
                    {area.label}
                  </SelectItem>
                ))}
              </SelectContent>
            </Select>
            <p className="text-sm text-muted-foreground mt-1">
              Fotoƒürafƒ±n hangi sayfada g√∂sterileceƒüini se√ßin
            </p>
          </div>

          {selectedUsageArea && filteredCategories.length > 0 && (
            <div>
              <Label htmlFor="category">Kategori</Label>
              <Select value={selectedCategory} onValueChange={setSelectedCategory}>
                <SelectTrigger>
                  <SelectValue placeholder="Kategori se√ßin" />
                </SelectTrigger>
                <SelectContent>
                  {filteredCategories.map((category) => (
                    <SelectItem key={category.id} value={category.id}>
                      {category.ad}
                    </SelectItem>
                  ))}
                </SelectContent>
              </Select>
              <p className="text-sm text-muted-foreground mt-1">
                Bu kullanƒ±m alanƒ± i√ßin mevcut kategoriler
              </p>
            </div>
          )}

          <div className="flex items-center space-x-2">
            <Switch
              id="addLogo"
              checked={addLogo}
              onCheckedChange={setAddLogo}
            />
            <Label htmlFor="addLogo" className="text-sm">
              Logo filigranƒ± ekle {firmaLogo ? '‚úÖ' : '‚ùå'}
            </Label>
          </div>

          <Button
            onClick={handleUpload} 
            disabled={isUploading || !photos || !selectedUsageArea}
            className="w-full"
          >
            <Upload className="h-4 w-4 mr-2" />
            {isUploading ? 'Y√ºkleniyor...' : 'Fotoƒüraflarƒ± Y√ºkle'}
          </Button>
          
          {!selectedUsageArea && photos && (
            <p className="text-sm text-amber-600 text-center">
              ‚ö†Ô∏è L√ºtfen bir kullanƒ±m alanƒ± se√ßin
            </p>
          )}
        </CardContent>
      </Card>
    </div>
  );
};